    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        File file = new File("math.txt");
        Scanner scanner;
        try {
            scanner = new Scanner(file);
            while (scanner.hasNextLine()) {
                questions.add(scanner.nextLine());
            }

        } catch (FileNotFoundException ex) {
            Logger.getLogger(Quiz2.class.getName()).log(Level.SEVERE, null, ex);
        }

        switch (questionNumber) {
            case 1:
                jTextField1.setText(questions.get(0));
                jRadioButton1.setText(questions.get(1));
                jRadioButton2.setText(questions.get(2));
                jRadioButton3.setText(questions.get(3));
                jRadioButton4.setText(questions.get(4));
                break;
            case 2:
                jTextField1.setText(questions.get(6));
                jRadioButton1.setText(questions.get(7));
                jRadioButton2.setText(questions.get(8));
                jRadioButton3.setText(questions.get(9));
                jRadioButton4.setText(questions.get(10));
                break;
            case 3:
                jTextField1.setText(questions.get(12));
                jRadioButton1.setText(questions.get(13));
                jRadioButton2.setText(questions.get(14));
                jRadioButton3.setText(questions.get(15));
                jRadioButton4.setText(questions.get(16));
                break;
            case 4:
                jTextField1.setText(questions.get(18));
                jRadioButton1.setText(questions.get(19));
                jRadioButton2.setText(questions.get(20));
                jRadioButton3.setText(questions.get(21));
                jRadioButton4.setText(questions.get(22));
                break;
            case 5:
                jTextField1.setText(questions.get(24));
                jRadioButton1.setText(questions.get(25));
                jRadioButton2.setText(questions.get(26));
                jRadioButton3.setText(questions.get(27));
                jRadioButton4.setText(questions.get(28));
                break;
            case 6:
                JOptionPane.showMessageDialog(this, "Tw√≥j wynik to: " + points + " /5", "Informacja", JOptionPane.INFORMATION_MESSAGE);
                return;

        }

        jButton2.setEnabled(false);
        jRadioButton1.setEnabled(true);
        jRadioButton2.setEnabled(true);
        jRadioButton3.setEnabled(true);
        jRadioButton4.setEnabled(true);

        bg.clearSelection();

    }//GEN-LAST:event_jButton2ActionPerformed

